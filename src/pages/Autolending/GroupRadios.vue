<template>
	<div>
		<h3 class="filter-title">
			Individuals or groups
		</h3>
		<kv-radio
			id="group-radio-both"
			radio-value="both"
			v-model="isGroup"
		>
			Both
		</kv-radio>
		<kv-radio
			id="group-radio-individual"
			radio-value="individual-only"
			v-model="isGroup"
		>
			Individuals only
		</kv-radio>
		<kv-radio
			id="group-radio-groups"
			radio-value="group-only"
			v-model="isGroup"
		>
			Groups only
		</kv-radio>
	</div>
</template>

<script>
import _get from 'lodash/get';
import gql from 'graphql-tag';
import KvRadio from '@/components/Kv/KvRadio';

export default {
	inject: ['apollo'],
	components: {
		KvRadio,
	},
	data() {
		return {
			isGroup: 'both',
		};
	},
	apollo: {
		query: gql`{
			autolending @client {
				currentProfile {
					loanSearchCriteria {
						filters {
							isGroup
						}
					}
				}
			}
		}`,
		preFetch: true,
		result({ data }) {
			const isGroup = _get(data, 'autolending.currentProfile.loanSearchCriteria.filters.isGroup');
			if (isGroup === true) {
				this.isGroup = 'group-only';
			} else if (isGroup === false) {
				this.isGroup = 'individual-only';
			} else {
				this.isGroup = 'both';
			}
		},
	},
	watch: {
		isGroup(groupOption, previousGroupOption) {
			if (groupOption !== previousGroupOption) {
				let isGroup = null;
				if (groupOption === 'group-only') {
					isGroup = true;
				} else if (groupOption === 'individual-only') {
					isGroup = false;
				}
				this.apollo.mutate({
					mutation: gql`mutation {
						autolending @client {
							editProfile(profile: {
								loanSearchCriteria: {
									filters: {
										isGroup: ${isGroup}
									}
								}
							})
						}
					}`,
				});
			}
		}
	},
};
</script>

