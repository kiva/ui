<template>
	<div>
		<h3 class="specific-filter-title">
			Countries
		</h3>
		<div class="row collapse">
			<div class="small-6 columns region-list">
				<ul>
					<li v-for="(region, name) in regions" :key="name">
						<button
							class="region-button"
							@click="openRegion = name"
							:aria-pressed="openRegion === name ? 'true' : 'false'"
						>
							{{ name }}
						</button>
					</li>
				</ul>
			</div>
			<div class="small-6 columns">
				<kv-expandable :skip-leave="true">
					<check-list
						v-if="currentRegion && currentRegion.length"
						:key="openRegion"
						:items="currentRegion"
						@change="onChange"
					/>
					<p v-else key="none">
						Pick a region
					</p>
				</kv-expandable>
			</div>
		</div>
	</div>
</template>

<script>
import _get from 'lodash/get';
import _groupBy from 'lodash/groupBy';
import _map from 'lodash/map';
import _sortBy from 'lodash/sortBy';
import _union from 'lodash/union';
import _without from 'lodash/without';
import gql from 'graphql-tag';
import KvExpandable from '@/components/Kv/KvExpandable';
import countryListQuery from '@/graphql/query/autolending/countryList.graphql';
import CheckList from './CheckList';

export default {
	inject: ['apollo'],
	components: {
		CheckList,
		KvExpandable,
	},
	data() {
		return {
			allCountries: [],
			currentIsoCodes: [],
			openRegion: '',
		};
	},
	computed: {
		countriesWithSelected() {
			return _map(this.allCountries, ({ isoCode, name, region }) => {
				return {
					id: isoCode,
					name,
					region,
					selected: this.currentIsoCodes.indexOf(isoCode) > -1,
				};
			});
		},
		regions() {
			return _groupBy(this.countriesWithSelected, 'region');
		},
		currentRegion() {
			return _sortBy(_get(this.regions, this.openRegion), 'name');
		},
	},
	apollo: {
		query: countryListQuery,
		preFetch: true,
		result({ data }) {
			this.allCountries = _map(_get(data, 'lend.countryFacets'), 'country') || [];
			this.currentIsoCodes = _get(data, 'autolending.currentProfile.loanSearchCriteria.filters.country') || [];
		},
	},
	methods: {
		onChange(checked, values) {
			const codes = Array.isArray(values) ? values : [values];
			if (checked) {
				// Add the values to the current ids
				this.changeCountries(_union(this.currentIsoCodes, codes));
			} else {
				// Remove the values from the current ids
				this.changeCountries(_without(this.currentIsoCodes, ...codes));
			}
		},
		changeCountries(countries) {
			this.apollo.mutate({
				mutation: gql`mutation($countries: [String]) {
					autolending @client {
						editProfile(profile: {
							loanSearchCriteria: {
								filters: {
									country: $countries
								}
							}
						})
					}
				}`,
				variables: {
					countries: countries.length ? countries : null,
				}
			});
		}
	},
};
</script>

