<template>
	<div class="loan-increment-radios">
		<h3 class="filter-title">
			Loan sizes
		</h3>
		<kv-radio
			id="loan-increment-any"
			radio-value="any"
			v-model="loanIncrement"
		>
			Any amounts
		</kv-radio>
		<kv-radio
			id="loan-increment-25"
			radio-value="25"
			v-model="loanIncrement"
		>
			Limit amounts to $25 per borrower
		</kv-radio>
	</div>
</template>

<script>
import _get from 'lodash/get';
import gql from 'graphql-tag';
import KvRadio from '@/components/Kv/KvRadio';

export default {
	inject: ['apollo'],
	components: {
		KvRadio,
	},
	data() {
		return {
			loanIncrement: 'any',
		};
	},
	apollo: {
		query: gql`{
			autolending @client {
				currentProfile {
					loanSearchCriteria {
						filters {
							loanLimit
						}
					}
				}
			}
		}`,
		preFetch: true,
		result({ data }) {
			this.loanIncrement = _get(data, 'autolending.currentProfile.loanSearchCriteria.filters.loanLimit') || 'any';
		},
	},
	watch: {
		loanIncrement(loanIncrement, previousLoanIncrement) {
			if (loanIncrement !== previousLoanIncrement) {
				this.apollo.mutate({
					mutation: gql`mutation {
						autolending @client {
							editProfile(profile: {
								loanSearchCriteria: {
									filters: {
										loanLimit: ${loanIncrement === 'any' ? null : loanIncrement}
									}
								}
							})
						}
					}`,
				});
			}
		}
	},
};
</script>

